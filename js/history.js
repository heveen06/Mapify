const container = document.getElementById("history-container");
const select = document.getElementById("countrySelect");


let heroSection;
let heroFlag;
let heroName;
let heroMeaning;
let heroLandmarks;
let timelineContainer;


function renderHistory(countryKey) {
  const data = historyData[countryKey];
  if (!data) {
    container.innerHTML = `<p class="no-history">No history available for this country.</p>`;
    return;
  }

 
  container.innerHTML = "";

  
  heroSection = document.createElement("section");
  heroSection.className = "country-hero";

  heroSection.innerHTML = `
    <div class="country-hero-inner">

      <div class="country-hero-image">
        <img id="heroFlagImg" src="images/flags/${data.flag}" alt="${data.name} Flag">

      </div>

      <div class="country-hero-content">
        <div class="country-region-tag">
          <div class="country-region-dot"></div>
          ${data.region || "Country"}
        </div>
        <h2 class="selected-country-name" id="heroCountryName">${data.name}</h2>

        <div class="country-hero-meta">
        
          <div class="flag-meaning">
            <h3 class="flag-meaning-title">Flag Meaning</h3>
            <p class="flag-meaning-text" id="heroFlagMeaning">
              ${data.flagMeaning || "No meaning provided."}
            </p>
          </div>

          <div class="country-landmarks">
            <h3 class="landmarks-title">Famous Landmarks</h3>
            <ul class="landmarks-list" id="heroLandmarkList">
              ${(data.landmarks || []).map((l) => `<li>${l}</li>`).join("")}
            </ul>
          </div>

        </div>
      </div>

    </div>
  `;

  container.appendChild(heroSection);


  timelineContainer = document.createElement("div");
  timelineContainer.className = "history-timeline";

  data.periods.forEach((period, index) => {
    const shortText = period.short || period.text || "";
    const longText = period.long || "";

    const hasLong = longText.trim().length > 0;

    const periodBox = document.createElement("div");
    periodBox.className = "history-period";

    periodBox.innerHTML = `
      <h3 class="period-title">${period.title}</h3>
      <p class="period-text short-text" id="short${index}">${shortText}</p>
      <p class="period-text long-text" id="long${index}" style="display:none;">${longText}</p>

      ${
        hasLong
          ? `<button class="read-more-btn" id="btn${index}" onclick="toggleRead(${index})">
               Read More
             </button>`
          : ""
      }
    `;

    timelineContainer.appendChild(periodBox);
  });

  container.appendChild(timelineContainer);
}


function toggleRead(i) {
  const longText = document.getElementById("long" + i);
  const btn = document.getElementById("btn" + i);

  if (longText.style.display === "none") {
    longText.style.display = "block";
    btn.textContent = "Read Less";
  } else {
    longText.style.display = "none";
    btn.textContent = "Read More";
  }
}


select.addEventListener("change", () => {
  const selected = select.value;
  if (!selected) {
    container.innerHTML = "";
    return;
  }
  renderHistory(selected);
});


function getCountryFromURL() {
  const url = new URL(window.location.href);
  return url.searchParams.get("country");
}

function init() {
  const country = getCountryFromURL();

  if (country && historyData[country]) {
    select.value = country;
    renderHistory(country);
  }
}

init();



